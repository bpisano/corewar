#include "vm.h"

int		convert_hexa_int(char a, char b)
{
	int		i;

	if (a > '9')
		i = (a - '0') * 16;
	else
		i = (a - 'A' + 10) * 16;
	if (b > '9')
		b += a - '0';
	else
		b += b - 'A' + 10;
	return (i);
}
